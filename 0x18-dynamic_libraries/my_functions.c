#include <stdio.h>

/**
 * add - add two integers
 * @a:input integer
 * @b: input integer
 * Return: what the function does
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substract two integers
 * @a:input integer
 * @b: input integer
 * Return: what the function does
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two integers
 * @a:input integer
 * @b: input integer
 * Return: what the function does
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two integers
 * @a:input integer
 * @b: input integer
 * Return: what the function does
 *
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - give remainder of two integers
 * @a:input integer
 * @b: input integer
 * Return: what the function does
 */

int mod(int a, int b)
{
	return (a % b);
}
